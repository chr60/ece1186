    private void browseRedLineFile(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_browseRedLineFile
       
        //open file browser
        JFileChooser PLCChoose = new JFileChooser();
        PLCChoose.setFileFilter(new FileNameExtensionFilter("CSV Files (.csv)", "csv"));
        int response = PLCChoose.showOpenDialog(null);
        if (response == JFileChooser.APPROVE_OPTION){
          this.redLineCSVTextField.setText(PLCChoose.getSelectedFile().getAbsolutePath());
        } else {
        }  
    }//GEN-LAST:event_browseRedLineFile

