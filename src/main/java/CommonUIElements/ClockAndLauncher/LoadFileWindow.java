    private void loadFiles(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loadFiles
        
        String[] fNames = {this.redLineCSVTextField.getText(), this.greenLineCSVTextField.getText()} ;
        String[] fOverrideNames = {this.redLineLinkageTextFile.getText(), this.greenLineLinkageTextField.getText() }; 
        
        if (fNames.length == 2 && fOverrideNames.length == 2){
            
            this.track.readCSV(fNames, fOverrideNames);
        }        
    }//GEN-LAST:event_loadFiles

